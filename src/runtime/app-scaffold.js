/**
 * @param {string} value
 * @returns {string}
 */
function packageNameFromAppName(value) {
    return value
        .trim()
        .toLowerCase()
        .replace(/[^a-z0-9._-]+/g, '-')
        .replace(/^-+|-+$/g, '')
        || 'tachyon-app';
}

/**
 * @param {string} value
 * @returns {string}
 */
function escapeJsString(value) {
    return JSON.stringify(value);
}

