/**
 * @param {string} route
 * @param {Response} response
 * @returns {Response}
 */
function withDocsHeaders(route, response) {
    const headers = new Headers(response.headers);
    headers.set('Cache-Control', route === '/openapi.json' ? 'no-cache, must-revalidate' : 'no-cache, must-revalidate');
    headers.set('Content-Security-Policy', docsContentSecurityPolicy());
    return new Response(response.body, {
        status: response.status,
        statusText: response.statusText,
        headers,
    });
}

async function docsStyles() {
    return (await getDocsAssets()).css;
}

async function docsScript() {
    const { script } = await getDocsAssets();
    return script
        .replace('__OPENAPI_PATH__', OPENAPI_JSON_PATH)
        .replace('__OPENAPI_DOCS_PATH__', OPENAPI_DOCS_PATH);
}

export default class OpenAPI {
    static registerRoutes() {
        Router.reqRoutes[OPENAPI_JSON_PATH] = {
            GET: async (request = new Request(`http://localhost${OPENAPI_JSON_PATH}`)) => {
                const response = Response.json(await buildSpec(request), {
                    status: 200,
                    headers: Router.getHeaders(request),
                });
                return withDocsHeaders(OPENAPI_JSON_PATH, response);
            },
        };
        const docsHandler = async (request = new Request(`http://localhost${OPENAPI_DOCS_PATH}`)) => {
            const response = new Response(docsHtml(), {
                status: 200,
                headers: {
                    ...Router.getHeaders(request),
                    'Content-Type': 'text/html; charset=utf-8',
                },
            });
            return withDocsHeaders(OPENAPI_DOCS_PATH, response);
        };
        Router.reqRoutes[OPENAPI_DOCS_PATH] = { GET: docsHandler };
        Router.reqRoutes[`${OPENAPI_DOCS_PATH}/`] = { GET: docsHandler };
        Router.reqRoutes[`${OPENAPI_DOCS_PATH}/app.css`] = {
            GET: async (request = new Request(`http://localhost${OPENAPI_DOCS_PATH}/app.css`)) => {
                const response = new Response(await docsStyles(), {
                    status: 200,
                    headers: {
                        ...Router.getHeaders(request),
                        'Content-Type': 'text/css; charset=utf-8',
                    },
                });
                return withDocsHeaders(`${OPENAPI_DOCS_PATH}/app.css`, response);
            },
        };
        Router.reqRoutes[`${OPENAPI_DOCS_PATH}/app.js`] = {
            GET: async (request = new Request(`http://localhost${OPENAPI_DOCS_PATH}/app.js`)) => {
                const response = new Response(await docsScript(), {
                    status: 200,
                    headers: {
                        ...Router.getHeaders(request),
                        'Content-Type': 'application/javascript; charset=utf-8',
                    },
                });
                return withDocsHeaders(`${OPENAPI_DOCS_PATH}/app.js`, response);
            },
        };
    }
}
